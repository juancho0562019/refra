class TrasladoDia {
  constructor(
    id,
    nombre,
    apellido,
    fecha,
    articulos,
    codInterno,
    idInterno,
    nomBodegaOrigen,
    nomBodegaDestino
  ) {
    this.id = id;
    this.nombre = nombre;
    this.apellido = apellido;
    this.fecha = fecha;
    this.articulos = articulos;
    this.codInterno = codInterno;
    this.idInterno = idInterno;
    this.nomBodegaOrigen = nomBodegaOrigen;
    this.nomBodegaDestino = nomBodegaDestino;
  }
}
export default TrasladoDia;
